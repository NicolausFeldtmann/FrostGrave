    constructor(world) {
        super().loadImg('img/witheWalker/Skeleton_Warrior_1/PNG/PNG Sequences/Running/0_Skeleton_Warrior_Running_000.png');
        this.world = world;
        this.loadImages(this.IMAGES_WALKING);
        this.loadImages(this.IMAGES_JUMPING);
        this.loadImages(this.IMAGES_HURT);
        this.loadImages(this.IMAGES_DYING);
        this.loadImages(this.IMAGES_SLASHING);
        this.loadImages(this.IMAGES_SLASH_MIDAIR);
        this.loadImages(this.IMAGES_THROW);
        this.loadImages(this.IMAGES_THROW_MIDAIR);
        this.loadImages(this.IMAGES_IDLE);
        this.alppyGravity();
        this.animate();
    }

    /**
     * all character animations
     */
    animate() {

        if (this.isAlive) {
            this.animationInterval = setInterval(() => {
                if (this.canMoveRight()) {
                    this.moveRight();
                }
                if (this.canMoveLeft()) {
                    this.moveLeft();
                }
                if (this.canWalk()) {
                    this.charWalk();
                }
                if (this.canJump()) {
                    this.jump();
                }
                if (this.canSlash()) {
                    this.charSlash();
                }
                if (this.canSlashAir()) {
                    this.charSlashAir();
                }
                if (this.canThrow()) {
                    this.charThrow();
                }

                if (this.canThrowAir()) {
                    this.charThrowAir();
                }

                if (this.canOverkill()) {
                    this.charOverkill();
                }

                this.world.camera_x = -this.x + 100;
            }, 1000 / 30);
        }
        this.setStateInterval();
    }

    /**
     * set char state check interval
     */
    setStateInterval() {
        this.stateInterval = setInterval(() => {
            if (this.isDeadAgain()) {
                if (this.isAlive) {
                    this.charDies();
                };
            } else if (this.isHurt()) {
                this.playAnimation(this.IMAGES_HURT);
            } else if (this.isAboveGround()) {
                this.playAnimation(this.IMAGES_JUMPING);
            } else if (this.canWalk()) {
                this.playAnimation(this.IMAGES_WALKING);
            } else {
                this.playAnimation(this.IMAGES_IDLE);
            }
        }, 100)
    }

    /**
     * set char staus to dead after timeout
     */
    isDead() {
        setTimeout(() => {
            this.isAlive = false;
        }, 1000);
    }

    /**
     * executs char dying event
     */
    charDies() {
        this.playAnimation(this.IMAGES_DYING);
        world.ouch.play();
        this.isDead();
        world.endGame();
        world.lostGame();
    }

    /**
     * executs char walk event
     */
    charWalk() {
        this.playAnimation(this.IMAGES_WALKING);
        world.snow1.play();
        world.snow2.play();
    }

    /**
     * executs char mele event
     */
    charSlash() {
        this.playAnimation(this.IMAGES_SLASHING);
        this.slash();
        world.slashSound.play();
    }

    /**
     * executs char mele mid air event
     */
    charSlashAir() {
        this.playAnimation(this.IMAGES_SLASH_MIDAIR);
        this.slash();
        world.slashSound.play();
    }

    /**
     * executs char throw event
     */
    charThrow() {
        if (this.isCool) {
            this.playAnimation(this.IMAGES_THROW);
            this.isCool = false;
            setTimeout(() => {
                this.isCool = true;
            }, 1000);
        }
    }

    /**
     * executs throw event mid air
     */
    charThrowAir() {
        if (this.isCool) {
            this.playAnimation(this.IMAGES_THROW_MIDAIR);
            this.isCool = false;
            setTimeout(() => {
                this.isCool = true;
            }, 1000);
        }
    }

    /**
     * executs overkill event
     */
    charOverkill() {
        if (this.isCool) {
            this.playAnimation(this.IMAGES_THROW);
        }
    }

    /**
     * detects move rigth command
     */
    canMoveRight() {
        return this.world.keyboard.RIGHT && this.x < this.world.level.level_end_x;
    }

    /**
     * detects move left command
     */
    canMoveLeft() {
        return this.world.keyboard.LEFT && this.x > 100;
    }

    /**
     * detects walk command
     */
    canWalk() {
        return (this.world.keyboard.RIGHT || this.world.keyboard.LEFT) && !this.isAboveGround();
    }

    /**
     * detects jump command
     */
    canJump() {
        return this.world.keyboard.SPACE && !this.isAboveGround();
    }

    /**
     * detects mele command
     */
    canSlash() {
        return this.world.keyboard.SLASH && !this.isAboveGround();
    }

    /**
     * detects mele command during jump
     */
    canSlashAir() {
        return this.world.keyboard.SLASH && this.isAboveGround();
    }

    /**
     * detects throw command
     */
    canThrow() {
        return this.world.keyboard.THROW && !this.isAboveGround();
    }

    /**
     * detects throw command during jump
     */
    canThrowAir() {
        return this.world.keyboard.THROW && this.isAboveGround();
    }

    /**
     * detects overkill command
     */
    canOverkill() {
        return this.world.keyboard.OVERKILL && !this.isAboveGround();
    }
